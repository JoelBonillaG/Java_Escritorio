    private void jTxtSearchByCardIDKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTxtSearchByCardIDKeyReleased
    String cardID = this.jTxtSearchByCardID.getText();
        System.out.println(cardID);
        
        System.out.println("Separador -----------------");
    if(!cardID.isEmpty()){
    
           DefaultTableModel model = new DefaultTableModel();
        
        model.addColumn("Cedula");
        model.addColumn("Nombre");
        model.addColumn("Apellido");
        
        try {
            this.students = apiStudent.getStudentsByCardID(cardID);
        } catch (Exception ex) {
            Logger.getLogger(StudentsForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        for(Student student : students){
        
        model.addRow(new Object[]{
        student.getCardID(),
        student.getName(),
        student.getLastName()
        });
        
        }
        
        this.jTblStudents.setModel(model);
        
    }else{
    updateStudentTable();
    }
    
    }//GEN-LAST:event_jTxtSearchByCardIDKeyReleased

