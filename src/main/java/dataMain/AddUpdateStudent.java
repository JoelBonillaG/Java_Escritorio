    private void jBtnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnSaveActionPerformed
        
        JOptionPane.showMessageDialog(null, jTxtName.getText());
        String cardID = jTxtCardID.getText();
        
        String name = jTxtName.getText();
        
        String lastName = jTxtLastName.getText();
        
        this.student = new Student(cardID, name, lastName);
        
        String result;
        
        if (this.jLblModeForm.equals("Crear")) {
            
            result = this.apiStudent.insertStudent(student);
        } else {
            
            result = this.apiStudent.updateStudent(student);
        }
        
        JOptionPane.showMessageDialog(null, result);
        
        this.setVisible(false);
        this.rootForm.updateStudentTable();
        this.rootForm.setVisible(true);
        
    }//GEN-LAST:event_jBtnSaveActionPerformed

